MODULE COOT
  IMPLICIT NONE
  REAL,DIMENSION(39)::X
  REAL,DIMENSION(1:39)::LOKI
  REAL,DIMENSION(39)::SUM1
  INTEGER::i,g,u
  REAL::SD,B
  REAL::HUT,JUT
  

CONTAINS
  SUBROUTINE show()

 
  


  OPEN(2,FILE="MAXIMUM.txt",STATUS="OLD")
 
  READ(2,*,END=45)X
45 CONTINUE
  HUT=SUM(X)/39
  CLOSE(2)

  OPEN(2,FILE="MAXIMUM.txt",STATUS="OLD")
  DO i=1,39
     READ(2,*,END=50)LOKI(i)
50 CONTINUE
     SD=(LOKI(i)-HUT)**2
     
     OPEN(10,FILE="SUM OF X.txt",STATUS="OLD")
     WRITE(10,*)SD
     
  END DO
  CLOSE(2)
  CLOSE(10)
 
  OPEN(10,FILE="SUM OF X.txt",STATUS="OLD")
  
  READ(10,*,END=200)SUM1
200 CONTINUE
  B=SUM(SUM1)
  
 
  JUT=SQRT(B/39)
 ! print*,Y
 ! print*,K
 
  CLOSE(10)
END SUBROUTINE show

  
END MODULE  COOT

